let num1 = 1
let num2 = 2

num1++ // vai acrescentar + 1 na variavel num1
console.log(num1) // vai printar 2
--num1 // subtrai 1 
console.log(num1) // printa 1

//ordem de prescedencia: o -- tem prioridade do que o +++

console.log(++num1 === num2--) // vai ser true pq o ++ é executado antes da ===, e o -- só é executado pós comparação
console.log(num1 === num2) // vai ser false

