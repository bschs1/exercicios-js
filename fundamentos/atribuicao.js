const a = 7 // = atribuição, msm coisa que python
let b = 3

b += a // ATRIBUIÇÃO ADITIVA, isso acrescenta o valor de 'a' pra 'b' isso é: b = b + a
console.log(b)

b -= 4 // ATRIBUIÇÃO SUBTRATIVA = b - b + 4
console.log (b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2 (resto da divisão de b por 2)
console.log(b)
